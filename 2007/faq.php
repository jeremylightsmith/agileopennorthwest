<h3>Frequently Asked Questions</h3>

<h4>Who can create an Open Space session?</h4>

<p>Anyone attending Agile Open northwest may create a session.</p>

<h4>What topics are suitable for an Open Space session?</h4>

<p>Any topic related to our conference theme is appropriate. Sessions may be 
   on technical or non-technical subjects, but should relate to working with
   agile methods in the real world.</p>

<h4>Do I have to be an expert in the topic to host a session?</h4>

<p>No, session conveners only need to have the level of expertise needed 
   to conduct the session. If you intend to teach other people about something, 
   we hope you actually know something about it. But you may choose to conduct 
   your session as a discussion among peers or even organize a session around 
   a topic that you know little about but would like to understand better. 
   Be sure to explain how you intend to conduct the session when you invite 
   people to attend it.</p>

<h4>Can I add a session after the Opening?</h4>

<p>Yes, just speak to one of the organizers. We will have announcements of new 
   sessions and changes at the start of each day and during lunch.</p>

<h4>Will there be a record of sessions?</h4>

<p>At the end of the conference, we'll publish an online record of what sessions 
   were held, who participated and what happened in them. The level of detail of
   this summary is up to the participants, but we will make a wiki available
   at the conference so that you can record information while it is fresh.</p>
   
<h4>Can we schedule sessions in the evening?</h4>

<p>We have scheduled sessions for Tuesday evening after dinner. We can use the 
rooms reserved for the conference until ~10:00 pm. After that you can schedule 
sessions in other places...the bars, hallways, guest rooms, use your imagination. :-)

<h4>Are meals provided?</h4>

<p>Yes, we'll have a continental breakfast and boxed lunches both days with a 
dinner on Tuesday and a reception on Wednesday.

<h4>Are accomodations available?</h4>

<p>The Kennedy School has 35 guest rooms. We've reserved a block of rooms for 
the event. Rooms outside our block are available on a first-come, first-served basis.

<h4>What equipment is provided for the sessions?</h4>

<p>All the rooms will have flip chart easels with tablets and markers. We'll set 
up one room with a screen and projector for any sessions that need them.

<h4>Will we have internet access?</h4>

<p>The Kennedy School has wireless access throughout the facility.

