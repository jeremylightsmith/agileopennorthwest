<p id="title">Northwest Agile Groups</p>

<p>The Pacific Nortwest is home to a large number of agile user groups.
These groups help make our area a strong center of agile software development 
and provide a solid base of attendance and support to Agile Open Northwest.</p>

<p>We are providing this listing of agile user groups in the Pacific Northwest
so that you can connect with people interested in agile software development 
after Agile Open Northwest. These groups meet for face-to-face discussions 
on a regular basis to share experiences and learn from each other.</p>

