<h3>About Agile Open Northwest</h3>

<p>Our goal is to serve the Agile community in the 
Pacific Northwest and beyond by providing a low-cost venue for 
experienced Agile practitioners to meet in self-organizing groups
where they can demonstrate and discuss, explore and expand their 
latest ideas, challenges, hopes, experiences, and experiments. 
We follow an Open Space Technology format to foster collaboration 
and maintain equal status among all participants. Anyone who has 
some degree of experience actually engaging Agile methods is 
welcome to attend.</p>

