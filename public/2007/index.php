<h3>Overview</h3>

<p>Agile Open Northwest, an alliance of agile practitioners in the
US Pacific Northwest region, presents Agile Open Northwest 2007.

<p>We invite 100 experienced, collaborative, committed agile practitioners 
from the Northwest and beyond to join us in tackling the issues around 
the theme of agile development in the real world.

<p>The Northwest has a wealth of practitioners with years 
of real-world experience with agile methods and self-organizing teams. 
Agile Open Northwest offers an opportunity to strengthen our community 
of practice and co-create the future for agile development. Your commitment to 
arriving at the beginning and staying until the end both days will ensure we build on 
conversation after conversation as we engage important questions like:

<ul>
	<li>Who practices Agile in the Northwest and what is its impact? 
	<li>What is "Agile" anyway? What does it look like in organizations?
	<li>What are the technical challenges facing Agile? 
	<li>How does Agile co-exist with project management, process control and 
	other governance structures?  
	<li>Can we adapt agile practices to our organizations without diluting them? 
	<li>Can agile methods work in big, risky projects? 
	<li>Can distributed teams use agile approaches? 
</ul

<p>This event is designed to allow agile practitioners
to meet in self-organizing groups where they can share their latest
ideas, challenges, hopes, experiences and experiments. We follow
an <a href="openspace.php">Open Space</a> format to foster collaboration and 
allow the conference to take its direction from the participants themselves.

<ul>
    <li><b>What:</b> An <a href="openspace.php">Open Space</a> event discussing
	agile practices and techniques.
	<li><b>Where:</b> The 
	<a href="http://www.mcmenamin.com/index.php?loc=57&category=Location%20Homepage">Kennedy School</a>, 
	5736 NE 33rd Ave, Portland, Oregon
	<li><b>When:</b> January 30 and 31, 2007
	<li><b>Who:</b> Anyone with some degree of experience in agile methods
	<li><b>Cost:</b> $150 per person, including lunch both days and dinner on the first day
</ul>

