<p id="title">Overview</p>
<p>Agile Open Northwest, an alliance of agile practitioners in the US Pacific Northwest region, presents Agile Open Northwest 2008.</p>
<p>Our first agile open event, <a href="../2007/index.php">Agile Open Northwest 2007</a>, was held last year and was a great success. This year, we once again invite 100 experienced, collaborative, committed agile practitioners from the Northwest and beyond to join us in tackling the issues around the theme of agile development in the real world.</p>
<p>The Northwest has a wealth of practitioners with years of real-world experience with agile methods and self-organizing teams. Agile Open Northwest offers an opportunity to strengthen our community of practice and co-create the future for agile development. Your commitment to arriving at the beginning and staying until the end both days will ensure we build on conversation after conversation as we engage important questions like:</p>
<ul>
  <li>Who practices Agile in the Northwest and what is its impact?</li>
  <li>What is "Agile" anyway? What does it look like in organizations?</li>
  <li>What are the technical challenges facing Agile?</li>
  <li>How does Agile co-exist with project management, process control and other governance structures?</li>
  <li>Can we adapt agile practices to our organizations without diluting them?</li>
  <li>Can agile methods work in big, risky projects?</li>
  <li>Can distributed teams use agile approaches?</li>
</ul>
<p>This event is designed to allow agile practitioners to meet in self-organizing groups where they can share their latest
  ideas, challenges, hopes, experiences and experiments. We follow an <a href="openspace.php">Open Space</a> format to foster collaboration and allow the conference to take its direction from the participants themselves.</p>
<ul>
  <li><b>What:</b> An <a href="openspace.php">Open Space</a> event discussing agile practices and techniques.</li>
  <li><b>Where:</b> <a href="http://www.seattlecenter.com">Seattle Center</a>, in several of the Northwest Rooms.</li>
  <li><b>When:</b> March 18 and 19, 2008</li>
  <li><b>Who:</b> Anyone with some degree of experience in agile methods</li>
  <li><b>Cost:</b> $100 per person, including lunch both days</li>
</ul>

