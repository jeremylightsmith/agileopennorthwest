<p id="title">About Open Space</p>
        
<p>You've heard of bar camps and unconferences...meet the start of them all! 
Agile Open Northwest runs on Open Space Technology (OST).

<p>Way back in 1983,
<a href="http://www.openspaceworld.com/brief_history.htm">Harrison Owen</a> 
made a discovery. He had spent a year organizing an international conference 
and, when it was over, everyone agreed that the best learning, information 
sharing and networking had occurred in the coffee breaks! Hmmmm. 

<p>After giving that some thought (and drinking two martinis), Owen went on to 
originate Open Space Technology, an approach to organizing meetings that allows 
groups of all sizes to self-organize, create an agenda and solve complex problems. 

<p>Over the last 20 or more years, the Open Space movement has grown to the 
point where many entire conferences have organized on its principles. Working 
with specific strategies to encourage and direct participation, groups of up to 
2000+ people successfully self-organize around complex problems, held meetings, 
and publish results.

<p>What a great fit for an Agile conference! OST delays all decisions about 
what sessions will be held, when they will be scheduled and who will attend 
them until the last responsible moment. If you have ever signed up for 
particular sessions in a traditional conference months in advance, only to 
discover that your needs and interests had changed by the time you got to 
the event, you'll appreciate the value of this!
        
<p id="title">Opening Space</p>

<p>The opening session of Agile Open Northwest takes place on the first morning 
with everyone attending. Your commitment to arriving at the beginning and staying 
until the end of the conference will ensure we build on conversation after conversation.
 
<p>During the opening session, a host will explain the Five Principles and 
One Law of Open Space. If you have questions, volunteers and hosts will offer answers.

<div class="boxed-text">
<p id="title" align="center">The Five Principles</p>
  <ul>
    <li>Whoever comes is the right people.</li>
    <li>Whatever happens is the only thing that could have.</li>
    <li>Whenever it starts is the right time.</li>
    <li>Whenever it's over, it's over. </li>
    <li>Wherever it happens is the right place.</li>
  </ul>

<p id="title" align="center">The Law of Personal Mobility</p>
  <p align="center">If you find yourself sitting where you can't learn or contribute, move yourself to a place where you can.</p>
</div>

<p id="title">Creating a Session</p>

<p>After the introduction, we will invite those who want to convene a session 
to come forward and present it. It's a simple process. You don't need to prepare 
papers, slides, or flip-charts. You will simply step forward and briefly describe 
the session you propose. You'll include just enough information to let people 
decide whether to attend - and no more. Generally, you need less than 30 seconds.

<p>After presenting a summary of your proposed session, you will select a time 
and place from those available on our wall-display and post a notice there. As 
each person does the same...Voila! The agenda is born!

<p>We ask that everyone attending our conference show up for the opening. It's 
important. You'll listen to the presentations and take note of those you would 
like to attend. As you listen, you may be inspired to present a session yourself. 
There are no deadlines or time limits: simply step up and describe your 
idea for a session.

<p id="title">Attending Sessions</p>

<p>At the end of the presentation, you will browse the notices on the wall and 
sign up to attend any sessions that interest you. Signing up for a session is 
optional. You may attend any session you like and even move between sessions 
(according to The Law).

<p id="title">Adding Sessions</p>

<p>You may add a session to the schedule at any time during the event by posting 
information about it in an available slot in the agenda. We'll have periodic 
announcements of any new or rescheduled sessions, but it's a good idea for 
all participants to keep an eye out for schedule changes as well.        

