        <p id="title">Frequently Asked Questions</p>
        <p id="subtitle">Who can create an Open Space session?</p>
        <p>Anyone attending Agile Open Northwest may create a session. </p>
        <p id="subtitle">What topics are suitable for an Open Space session?</p>
        <p>Any topic related to our conference theme is appropriate. Sessions may be on technical or non-technical subjects, but should relate to working with agile methods in the real world. </p>
        <p id="subtitle">Do I have to be an expert in the topic to host a session?</p>
        <p> No, session conveners only need to have the level of expertise needed to conduct the session. If you intend to teach other people about something, we hope you actually know something about it. But you may choose to conduct your session as a discussion among peers or even organize a session around a topic that you know little about but would like to understand better. Be sure to explain how you intend to conduct the session when you invite people to attend it. </p>
        <p id="subtitle">Can I add a session after the Opening?</p>
        <p> Yes, just speak to one of the organizers. We will have announcements of new sessions and changes at the start of each day and during lunch. </p>
        <p id="subtitle">Will there be a record of sessions?</p>
        <p> At the end of the conference, we'll publish an online record of what sessions were held, who participated and what happened in them. The level of detail of this summary is up to the participants, but we will make a wiki available at the conference so that you can record information while it is fresh. </p>
        <p id="subtitle">Can we schedule sessions in the evening?</p>
        <p> We have optional night-owl sessions scheduled for 7:30 in the evening
in the Seattle Center rooms. Of course, you can schedule sessions in
other places like coffeeshops, restaurants, bars... use your
imagination. :-) </p>
        <p id="subtitle">Are meals provided?</p>

        <p> We'll have a continental breakfast and boxed lunches both
        days. Appetizers and beverages will be served at a short
        reception on Tuesday evening (cash-bar available). Your evening meal is up to you,
        with a number of restaurants to choose from within walking
        distance. </p>

